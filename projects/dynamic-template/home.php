		<h2>Commercial Plan Development (Site Plans)</h2>
		<p>The Community Development Office coordinates the Commercial Development Process through the application of County Ordinance Chapter 15.5. In general, this process coordinates the state and local code based requirements for development through a single submittal and review process. Contact the Community Development Coordinator with questions.</p>
		<h3 class="section-two">Site Plan Committee?</h3>
		<p>The Site Plan Committee is compromised of representatives from each County Department and State Agency responsible for the review of commercial developments.  The committee is available to meet with applicants once a month to discuss potential development projects and discuss projects under review. The meetings are held on the first Monday of each month at 3 p.m. Please Contact the Community Development Coordinator to request time on a meeting agenda.</p>
